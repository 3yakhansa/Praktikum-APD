#Nama dan NIM
nama = input("nama: ")
nim = input("NIM: ")
harga = int(input("harga beras: "))
print(nama + " " + "dengan NIM" + " " + str(nim) + " " + "ingin membeli beras dengan harga" + " " + "Rp." + str(harga))

#Harga Beras Mawar
berasMawar = (harga) - (harga)*(11/100)
print("Jika ia membeli beras Mawar ia akan membayar" + " " + "Rp." + str(berasMawar) + " " + "setelah mendapat diskon 11%")

